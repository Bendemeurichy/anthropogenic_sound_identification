def fig_classification_per_separator() -> go.Figure:
    """Grid of small subplots, one per (separator, classifier) pair.
    Inside each cell:

      * X-axis groups the two input regimes (clean-COI input vs. mixture input).
      * Two bars per group: F1 without separator (grey) vs. F1 with separator (blue).

    Reading the grid:
      * Down a column = compare separator models for a fixed classifier.
      * Across a row  = compare classifiers for a fixed separator.

    Within each cell:
      * Left vs. right group = the input-distribution shift cost.
      * Grey vs. blue inside a group = whether the separator helps or hurts
        on that input regime.
    """
    classifiers = CLASSIFIERS_AIRPLANE
    separators  = SEPARATORS_AIRPLANE

    n_rows = len(separators)
    n_cols = len(classifiers)

    titles = []
    for sep_label, _, _ in separators:
        for cls in classifiers:
            titles.append(f"<b>{sep_label}</b> · {_cls_label(cls)}")

    fig = make_subplots(
        rows=n_rows, cols=n_cols,
        subplot_titles=titles,
        shared_yaxes=True, shared_xaxes=True,
        horizontal_spacing=0.05, vertical_spacing=0.10,
    )

    legend_done = False
    for r, (sep_label, sep_dir, _risoux_dir) in enumerate(separators, 1):
        for c, cls in enumerate(classifiers, 1):
            d = _load_classifier_json(sep_dir, cls)

            f1_clean_nosep = _metric(d, "f1_score", "clean_cls")
            f1_clean_sep   = _metric(d, "f1_score", "clean_sep_cls")
            f1_mix_nosep   = _metric(d, "f1_score", "mix_cls")
            f1_mix_sep     = _metric(d, "f1_score", "mix_sep_cls")

            no_sep_vals   = [f1_clean_nosep, f1_mix_nosep]
            with_sep_vals = [f1_clean_sep,   f1_mix_sep]

            no_sep_text   = [f"{v:.2f}" if not np.isnan(v) else "n/a"
                             for v in no_sep_vals]
            with_sep_text = [f"{v:.2f}" if not np.isnan(v) else "n/a"
                             for v in with_sep_vals]

            show_leg = not legend_done
            fig.add_trace(
                go.Bar(
                    x=INPUT_LABELS, y=no_sep_vals,
                    name="No separator",
                    legendgroup="no_sep", showlegend=show_leg,
                    marker=dict(color=COL_NO_SEP,
                                line=dict(color="rgba(0,0,0,0.25)", width=0.6)),
                    text=no_sep_text, textposition="outside",
                    textfont=dict(family=FONT_FAMILY, size=10, color="#1f1f1f"),
                    cliponaxis=False,
                ),
                row=r, col=c,
            )
            fig.add_trace(
                go.Bar(
                    x=INPUT_LABELS, y=with_sep_vals,
                    name="With separator",
                    legendgroup="with_sep", showlegend=show_leg,
                    marker=dict(color=COL_WITH_SEP,
                                line=dict(color="rgba(0,0,0,0.25)", width=0.6)),
                    text=with_sep_text, textposition="outside",
                    textfont=dict(family=FONT_FAMILY, size=10, color="#1f1f1f"),
                    cliponaxis=False,
                ),
                row=r, col=c,
            )
            legend_done = True

            # Delta annotations: (with_sep - no_sep) per input regime,
            # coloured green if the separator helps, red if it hurts.
            for x_lbl, v_no, v_with in zip(INPUT_LABELS, no_sep_vals, with_sep_vals):
                if np.isnan(v_no) or np.isnan(v_with):
                    continue
                delta = v_with - v_no
                if abs(delta) < 0.005:
                    color = DELTA_NEU
                    arrow = "≈"
                elif delta > 0:
                    color = DELTA_POS
                    arrow = "▲"
                else:
                    color = DELTA_NEG
                    arrow = "▼"
                y_anchor = max(v_no, v_with) + 0.10
                fig.add_annotation(
                    x=x_lbl, y=y_anchor,
                    xref=("x" if (r == 1 and c == 1)
                          else f"x{((r - 1) * n_cols) + c}"),
                    yref=("y" if (r == 1 and c == 1)
                          else f"y{((r - 1) * n_cols) + c}"),
                    text=f"<b>{arrow} {delta:+.2f}</b>",
                    showarrow=False, yanchor="bottom",
                    font=dict(family=FONT_FAMILY, size=11, color=color),
                )

            fig.update_yaxes(range=[0, 1.20], row=r, col=c,
                             title_text="F1 score" if c == 1 else None)

    fig.update_layout(
        barmode="group", bargap=0.30, bargroupgap=0.10,
        title=dict(text="<b>Downstream classification F1 — separator on/off vs. input regime</b>"
                        "<br><sup>Within each cell: left group = clean-COI input, "
                        "right group = mixture input; "
                        "grey = no separator, blue = separator + classifier</sup>"),
        height=260 * n_rows + 120, width=1200,
        **LAYOUT_BASE,
    )
    _styled_subplot_axes(fig, n_rows=n_rows, n_cols=n_cols)
    return fig


